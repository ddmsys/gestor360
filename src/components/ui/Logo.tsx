interface LogoProps {
  /** full = mark + wordmark | mark = apenas o ícone 360 */
  variant?: "full" | "mark";
  /** light = wordmark escuro (fundo claro) | dark = wordmark branco (fundo escuro) */
  theme?: "light" | "dark";
  /** Altura em px. Padrão: 40 */
  height?: number;
  className?: string;
}

export function Logo({
  variant = "full",
  theme = "light",
  height = 40,
  className,
}: LogoProps) {
  if (variant === "mark") {
    return <LogoMark height={height} className={className} />;
  }

  // Versão completa para fundo claro: usa o arquivo SVG original
  if (theme === "light") {
    const width = Math.round(height * (835.46 / 466.16));
    return (
      // eslint-disable-next-line @next/next/no-img-element
      <img
        src="/logo_ogestor.svg"
        alt="oGestor360®"
        width={width}
        height={height}
        className={className}
        draggable={false}
      />
    );
  }

  // Versão para fundo escuro: preserva o SVG oficial sobre uma base clara.
  const width = Math.round(height * (835.46 / 466.16));
  return (
    <span className={`inline-flex rounded-[var(--radius-sm)] bg-white px-2 py-1 ${className ?? ""}`}>
      {/* eslint-disable-next-line @next/next/no-img-element */}
      <img
        src="/logo_ogestor.svg"
        alt="oGestor360®"
        width={width}
        height={height}
        draggable={false}
      />
    </span>
  );
}
